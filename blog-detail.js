// Get blog ID from URL parameter
function getBlogIdFromURL() {
    const urlParams = new URLSearchParams(window.location.search);
    return parseInt(urlParams.get('id')) || 1;
}

// Load blog content
function loadBlogDetail() {
    const blogId = getBlogIdFromURL();
    const blog = blogs.find(b => b.id === blogId);
    
    if (blog) {
        // Update page title
        document.title = `${blog.title} | शेर-शराबा`;
        
        // Update header
        document.getElementById('blogCategory').textContent = blog.category;
        document.getElementById('blogTitle').textContent = blog.title;
        document.getElementById('blogDate').textContent = blog.date;
        document.getElementById('blogAuthor').textContent = blog.author;
        
        // Update image
        document.getElementById('blogImage').src = blog.image;
        document.getElementById('blogImage').alt = blog.title;
        
        // Update content
        document.getElementById('blogContent').innerHTML = blog.content;
        
        // Update author name
        document.getElementById('authorName').textContent = blog.author;
    } else {
        // If blog not found, redirect to blogs page
        window.location.href = 'blogs.html';
    }
}

// Share functions
function shareOnFacebook() {
    const url = encodeURIComponent(window.location.href);
    window.open(`https://www.facebook.com/sharer/sharer.php?u=${url}`, '_blank');
}

function shareOnTwitter() {
    const blogId = getBlogIdFromURL();
    const blog = blogs.find(b => b.id === blogId);
    const text = encodeURIComponent(blog ? blog.title : '');
    const url = encodeURIComponent(window.location.href);
    window.open(`https://twitter.com/intent/tweet?text=${text}&url=${url}`, '_blank');
}

function shareOnWhatsApp() {
    const blogId = getBlogIdFromURL();
    const blog = blogs.find(b => b.id === blogId);
    const text = encodeURIComponent(blog ? `${blog.title} - ${window.location.href}` : window.location.href);
    window.open(`https://wa.me/?text=${text}`, '_blank');
}

function shareOnLinkedIn() {
    const url = encodeURIComponent(window.location.href);
    window.open(`https://www.linkedin.com/sharing/share-offsite/?url=${url}`, '_blank');
}

// Load blog when page loads
document.addEventListener('DOMContentLoaded', loadBlogDetail);
